DROP TABLE IF EXISTS provincia_cl CASCADE;
CREATE TABLE provincia_cl (
  id_pr INT NOT NULL PRIMARY KEY, -- ID provincia
  id_re INT NOT NULL, -- ID region asociada
  str_descripcion varchar(30) NOT NULL, -- Nombre descriptivo 
  num_comunas INT NOT NULL, -- Numero de comunas
  FOREIGN KEY (id_re) REFERENCES region_cl(id_re)
);

